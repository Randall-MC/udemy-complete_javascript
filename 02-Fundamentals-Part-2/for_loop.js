// ---------- Iteration: The For Loop ----------
// The for loop statement has 3 parts
// The first part is the initial value of a counter
// The second part is a logical condition that is evaluated before each iteration of the loop, so keeps running while the condition is TRUE
// The third part is increase the counter by one
for (let rep = 1; rep <= 10; rep++) {
  console.log("🏋🏼‍♂️ Lifting weights repetition " + rep);
}
